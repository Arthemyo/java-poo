
public class TestaCaracteres {

	public static void main(String[] args) {
		char letra = 'a';
		System.out.println(letra);
		
		char valor = 75;
		System.out.println(valor);
		
		valor = (char) (valor + 3);
		System.out.println(valor); 
		
		
		String palavra = "Bem vindo!";
		System.out.println(palavra);
		
		palavra = "Bem vindo! " + 2022;
		System.out.println(palavra);
		
		

	}

}
